#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	int		i;
	int		len;

	if (!s || !f)
		return (NULL);
	len = ft_strlen(s);
	i = 0;
	if (!(str = ft_strnew(len)))
		return (NULL);
	while (len--)
	{
		str[i] = f(i, ((char*)s)[i]);
		i++;
	}
	return (str);
}
