#include "libft.h"

char	*ft_strlowcase(char *s)
{
	int i;

	if (!s)
		return (NULL);
	i = -1;
	while (s[++i] != '\0')
		if (s[i] >= 'A' && s[i] <= 'Z')
			s[i] = s[i] + 32;
	return (s);
}
