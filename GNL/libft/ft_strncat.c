#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	size_t i;
	size_t j;

	i = 0;
	j = ft_strlen(dest);
	while (src[i] != '\0' && i < n)
		dest[j++] = src[i++];
	dest[j] = '\0';
	return (dest);
}
