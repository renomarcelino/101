#include "libft.h"

size_t		ft_lstcount(t_list *lst)
{
	int		i;

	if (!lst)
		return (0);
	i = 0;
	while (lst->next != NULL)
	{
		lst = lst->next;
		i++;
	}
	return (i);
}
